fn fold_horizontally(
    dots: HashSet<(usize, usize)>,
    fold_position: usize,
) -> HashSet<(usize, usize)> {
    dots.iter()
        .filter_map(|(x, y)| {
            if *x > fold_position {
                Some((fold_position - (*x - fold_position), *y))
            } else if *x == fold_position {
                None
            } else {
                Some((*x, *y))
            }
        })
        .collect()
}

fn fold_vertically(dots: HashSet<(usize, usize)>, fold_position: usize) -> HashSet<(usize, usize)> {
    dots.iter()
        .filter_map(|(x, y)| {
            if *y > fold_position {
                Some((*x, fold_position - (*y - fold_position)))
            } else if *y == fold_position {
                None
            } else {
                Some((*x, *y))
            }
        })
        .collect()
}

fn print_dots(dots: &HashSet<(usize, usize)>) {
    let minimum_x = dots.iter().min_by_key(|(x, _y)| x).unwrap().0;
    let maximum_x = dots.iter().max_by_key(|(x, _y)| x).unwrap().0;
    let minimum_y = dots.iter().min_by_key(|(_x, y)| y).unwrap().1;
    let maximum_y = dots.iter().max_by_key(|(_x, y)| y).unwrap().1;
    for y in minimum_y..=maximum_y {
        for x in minimum_x..=maximum_x {
            if dots.get(&(x, y)).is_some() {
                print!("#");
            } else {
                print!(".");
            }
        }
        println!();
    }
}
